	public static void forCycle() {

		int sum = 0;
		long start = System.currentTimeMillis();
		for (Integer integer : list) {
			sum += integer;
		}
		System.out.println("forCycle  sum = " + sum + "  耗时  " + (System.currentTimeMillis() - start));

		start = System.currentTimeMillis();
		sum = 0;
		ListIterator<Integer> iterator = list.listIterator();
		while (iterator.hasNext()) {
			sum += iterator.next();
		}
		System.out.println("whileCycle  sum  = " + sum + "  耗时  " + (System.currentTimeMillis() - start));

		start = System.currentTimeMillis();
		sum = 0;
		sum = next(list, sum);
		System.out.println("递归  sum = " + sum + "  耗时  " + (System.currentTimeMillis() - start));

	}

	private static Integer next(List<Integer> list, int sum) {
		if (list.isEmpty()) {
			System.out.println("next.[list, sum] list is empty");
			return sum;
		}
//		System.out.println("next.first [list = " + list + ", sum = " + sum);
		sum += list.get(0);
		if (list.size() > 0) {
			list.remove(0);
			sum = next(list, sum);
//			System.out.println("next. after [list = " + list + ", sum = " + sum);
		}
		return sum;
	}

